# Vamos a crear um diccionrio que é uma estrutura de almacenamento de dados
# que trabalha em pares com uma key e um valor asignado a ela
# util quando queremos guardar caracteristicas de alguma coisa que não tem comportamento(metodos ou function)
pessoa = {
    "nome":'Joao',
    "edade": 25,
    "sexo": 'Masculino'
    }  #Exemplo de um diccionario guardado em una variable pessoa, ele almacena alguns dados
print(pessoa["nome"]) #imprimimos el valor almacenado en la key nome